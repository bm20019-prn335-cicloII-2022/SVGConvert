namespace SVGConvert;

/// <summary>
/// Formatos disponibles para la conversion
/// </summary>
public enum Format
{
    /// <summary>
    /// Portable Network Graphics
    /// </summary>
    Png,
    /// <summary>
    /// Joint Photographic Experts Group
    /// </summary>
    Jpeg,
    /// <summary>
    /// Tagged Image File Format
    /// </summary>
    Tiff,
    /// <summary>
    /// Windows bitmap
    /// </summary>
    Bmp,
    /// <summary>
    /// EMF Metafile Format
    /// </summary>
    Emf,
    /// <summary>
    /// Exchangeable image file format
    /// </summary>
    Exif,
    /// <summary>
    /// Windows Metafile
    /// </summary>
    Wmf
}